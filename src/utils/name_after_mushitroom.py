    def get_random_name(cls, name: str) -> str:
        """
        랜덤한 닉네임을 생성하여 반환합니다.
        """
        adj = random.choice(cls.ADJECTIVES)

        # 10% 확률로 뒤에 숫자를 붙여서 유니크함 더하기 (선택사항)
